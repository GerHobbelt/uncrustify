// based_pointers2.cpp

// compile with: /EHsc
#include <iostream>

int a1[] = { 1, 2, 3 };
int a2[] = { 10, 11, 12 };
int *pBased;

typedef int __based (pBased) * pBasedPtr;

using namespace std;
int main()
{
   pBased = &a1[0];
   pBasedPtr pb = 0;

   cout << *pb << endl;
   cout << *(pb + 1) << endl;

   pBased = &a2[0];

   cout << *pb << endl;
   cout << *(pb + 1) << endl;
}

